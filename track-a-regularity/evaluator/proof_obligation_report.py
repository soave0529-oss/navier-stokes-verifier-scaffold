from __future__ import annotations

import argparse
import json
import sys
from dataclasses import dataclass
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
DEFAULT_MARKDOWN_OUTPUT = ROOT / "track-a-regularity/reports/lemma_0252_proof_obligation_graph.md"
DEFAULT_JSON_OUTPUT = ROOT / "track-a-regularity/reports/lemma_0252_proof_obligation_graph.json"
DEFAULT_OUTPUT = DEFAULT_MARKDOWN_OUTPUT


@dataclass(frozen=True)
class ObligationNode:
    key: str
    label: str
    status: str
    supplied_as_assumption: bool
    proved_in_current_artifact: bool
    blocker: str


@dataclass(frozen=True)
class Lemma0252GraphReport:
    lemma_id: str
    candidate_status: str
    active_candidate: bool
    lean_artifact: str
    graph_doc: str
    formal_map_doc: str
    nodes: tuple[ObligationNode, ...]

    @property
    def blocking_nodes(self) -> tuple[ObligationNode, ...]:
        return tuple(
            node
            for node in self.nodes
            if node.status in {"missing_mechanism", "guardrail_only"}
            and not node.proved_in_current_artifact
        )


